class Solution
{
    //Function to find the maximum profit and the number of jobs done.
    int[] JobScheduling(Job a[], int n)
    {
        Arrays.sort(a, (a1, a2) -> a2.profit - a1.profit);
        int profit = 0, c = 0;
        
        boolean[] isFree = new boolean[101];
        Arrays.fill(isFree, true);
        
        for (Job job: a) {
            for (int time = job.deadline; time >= 1; time--) {
                if (isFree[time]) {
                    profit += job.profit;
                    c++;
                    isFree[time] = false;
                    break;
                } 
            }
        }
        return new int[] {c, profit};
    }
}

